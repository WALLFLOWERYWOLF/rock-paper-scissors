function getComputerChoice() {
  let randomNumber = Math.floor(Math.random() * 3);
  switch (randomNumber) {
    case 0:
      return "rock";
    case 1:
      return "paper";
    case 2:
      return "scissors";
  }
}

function getHumanChoice() {
  let humanChoice = prompt("Enter 'Rock', 'Paper', or 'Scissors'");
  humanChoice = humanChoice.toLowerCase();
  return humanChoice;
}
